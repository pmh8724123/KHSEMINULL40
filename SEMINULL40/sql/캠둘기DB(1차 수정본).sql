/* TABEL 생성 */

-- 회원 정보 관련
CREATE TABLE UNIVERSITY (
    UNI_NO   NUMBER       PRIMARY KEY,
    UNI_NAME VARCHAR2(30) NOT NULL,
    STATUS   CHAR      DEFAULT 'Y' CHECK (STATUS IN('Y' ,'N'))
);
CREATE TABLE DEPARTMENT (
    DEPT_NO   NUMBER       PRIMARY KEY,
    UNI_NO    NUMBER       REFERENCES UNIVERSITY NOT NULL,
    DEPT_NAME VARCHAR2(30) NOT NULL,
    UNIQUE(UNI_NO, DEPT_NAME)
);
CREATE TABLE MEMBER (
    MEM_NO      NUMBER        PRIMARY KEY,
    MEM_ID      VARCHAR2(20)  NOT NULL UNIQUE,
    MEM_PW      VARCHAR2(100) NOT NULL,
    STUDENT_NO  VARCHAR2(20),
    MEM_NAME    VARCHAR2(20)  NOT NULL,
    DEPT_NO     NUMBER        REFERENCES DEPARTMENT NOT NULL,
    PHONE       VARCHAR2(13)  UNIQUE,
    CREATE_DATE DATE          DEFAULT SYSDATE,
    STATUS      CHAR(1)       DEFAULT 'Y' CHECK (STATUS IN('Y', 'B' ,'N'))
);
CREATE TABLE AUTHORITIES (
    AUTHORITY VARCHAR2(20),
    MEM_NO    NUMBER       REFERENCES MEMBER,
    UNI_NO    NUMBER       REFERENCES UNIVERSITY,
    PRIMARY KEY(AUTHORITY, MEM_NO, UNI_NO)
);

-- 공지사항 및 게시판
CREATE TABLE NOTICE (
    NOTICE_NO      NUMBER        PRIMARY KEY,
    UNI_NO         NUMBER        REFERENCES UNIVERSITY,
    NOTICE_TITLE   VARCHAR2(50)  NOT NULL,
    NOTICE_CONTENT VARCHAR2(300),
    CREATE_DATE    DATE          DEFAULT SYSDATE,
    STATUS         CHAR(1)       DEFAULT 'Y' CHECK (STATUS IN('Y', 'N'))
);
CREATE TABLE BOARD_TYPE (
    BTYPE_NO   NUMBER PRIMARY KEY,
    BTYPE_NAME VARCHAR2(100)
);
CREATE TABLE UNI_BOARD_TYPE (
    UBTYPE_NO NUMBER PRIMARY KEY,
    UNI_NO    NUMBER REFERENCES UNIVERSITY,
    BTYPE_NO  NUMBER REFERENCES BOARD_TYPE,
    UNIQUE(UNI_NO, BTYPE_NO)
);
CREATE TABLE BOARD (
    BOARD_NO      NUMBER        PRIMARY KEY,
    UBTYPE_NO     NUMBER        DEFAULT 1 REFERENCES UNI_BOARD_TYPE,
    BOARD_TITLE   VARCHAR2(100) NOT NULL,
    BOARD_CONTENT VARCHAR2(300),
    BOARD_WRITER  NUMBER        REFERENCES MEMBER NOT NULL,
    VIEW_COUNT    NUMBER        DEFAULT 0,
    CREATE_DATE   DATE          DEFAULT SYSDATE,
    STATUS        CHAR(1)       DEFAULT 'Y' CHECK (STATUS IN('Y', 'N'))
);
CREATE TABLE BOARD_LIKE(
    BOARD_NO NUMBER REFERENCES BOARD,
    MEM_NO   NUMBER REFERENCES MEMBER,
    PRIMARY KEY(BOARD_NO, MEM_NO)
);
CREATE TABLE REPLY(
    REPLY_NO      NUMBER       PRIMARY KEY,
    BOARD_NO      NUMBER       REFERENCES BOARD NOT NULL,
    REPLY_CONTENT VARCHAR(200) NOT NULL,
    REPLY_WRITER  NUMBER       REFERENCES MEMBER NOT NULL,
    CREATE_DATE   DATE         DEFAULT SYSDATE,
    STATUS        CHAR(1)      DEFAULT 'Y' CHECK (STATUS IN('Y', 'N'))
);
CREATE TABLE REPLY_LIKE(
    REPLY_NO NUMBER REFERENCES REPLY,
    MEM_NO   NUMBER REFERENCES MEMBER,
    PRIMARY KEY(REPLY_NO, MEM_NO)
);

-- 신고
CREATE TABLE REPORT_REASON(
    REASON_NO   NUMBER       PRIMARY KEY,
    REASON_NAME VARCHAR2(40)
);
CREATE TABLE REPORT(
    REPORT_NO      NUMBER        PRIMARY KEY,
    REPORT_MEM     NUMBER        REFERENCES MEMBER NOT NULL,
    REASON_NO      NUMBER        DEFAULT 0 REFERENCES REPORT_REASON,
    REASON_CONTENT VARCHAR2(100),
    TARGET_TYPE    VARCHAR2(20)  NOT NULL,
    TARGET_NO      NUMBER        NOT NULL,
    UNIQUE(REPORT_MEM, TARGET_TYPE, TARGET_NO)
);

-- 출석체크 및 포인트
CREATE TABLE ATTENDANCE(
    MEM_NO      NUMBER REFERENCES MEMBER PRIMARY KEY,
    ATTEND_DAYS DATE,
    COUNT       NUMBER DEFAULT 0
);
CREATE TABLE USER_POINT(
    MEM_NO  NUMBER REFERENCES MEMBER PRIMARY KEY,
    POINT   NUMBER DEFAULT 0
);

-- 강의 및 시간표
CREATE TABLE LECTURE(
    LECTURE_NO     NUMBER       PRIMARY KEY,
    DEPT_NO        NUMBER       REFERENCES DEPARTMENT NOT NULL,
    LECTURE_NAME   VARCHAR2(20) NOT NULL,
    PROFESSOR_NAME VARCHAR2(20) NOT NULL,
    UNIQUE(LECTURE_NAME, PROFESSOR_NAME)
);
CREATE TABLE LECTURE_ASSESSMENT(
    ASSE_NO     NUMBER PRIMARY KEY,
    MEM_NO      NUMBER REFERENCES MEMBER NOT NULL,
    LECTURE_NO  NUMBER REFERENCES LECTURE NOT NULL,
    HOMEWORK    NUMBER DEFAULT 1,
    GRADE       NUMBER DEFAULT 1, 
    TOTAL_SCORE NUMBER DEFAULT 5,
    UNIQUE(MEM_NO, LECTURE_NO)
);
CREATE TABLE SCHEDULE_TITLE(
    SCHTITLE_NO NUMBER PRIMARY KEY,
    SCHTITLE_NAME VARCHAR(30)
);
CREATE TABLE SCHEDULE(
    SCHEDULE_NO  NUMBER PRIMARY KEY,
    MEM_NO       NUMBER REFERENCES MEMBER NOT NULL,
    SCHTITLE_NO  NUMBER REFERENCES SCHEDULE_TITLE,
    LECTURE_NO   NUMBER REFERENCES LECTURE NOT NULL,
    SCHEDULE_DAY NUMBER NOT NULL,
    START_TIME   NUMBER NOT NULL,
    END_TIME     NUMBER NOT NULL
);

-- 친구
CREATE TABLE FRIENDS(
    SENDER_NO   NUMBER  REFERENCES MEMBER,
    RECEIVER_NO NUMBER  REFERENCES MEMBER,
    STATUS      CHAR(1) DEFAULT 'N' CHECK (STATUS IN ('Y', 'N')),
    PRIMARY KEY(SENDER_NO, RECEIVER_NO)
);

-- 채팅
CREATE TABLE CHAT(
    CHAT_NO NUMBER PRIMARY KEY,
    CHAT_TITLE VARCHAR2(30) NOT NULL,
    STATUS CHAR(1) DEFAULT 'Y' CHECK (STATUS IN ('Y', 'N'))
);
CREATE TABLE CHAT_MESSAGE(
    MSG_NO       NUMBER        PRIMARY KEY,
    CHAT_NO      NUMBER        REFERENCES CHAT NOT NULL,
    CHAT_MESSAGE VARCHAR2(100) NOT NULL,
    CHAT_WRITER  NUMBER        REFERENCES MEMBER NOT NULL,
    CREATE_DATE  DATE          DEFAULT SYSDATE
);
CREATE TABLE CHAT_JOIN(
    MEM_NO  NUMBER REFERENCES MEMBER,
    CHAT_NO NUMBER REFERENCES CHAT,
    PRIMARY KEY(MEM_NO, CHAT_NO)
);

-- 첨부파일
CREATE TABLE FILES(
    FILE_NO NUMBER PRIMARY KEY,
    ORIGIN_NAME VARCHAR2(225),
    CHANGE_NAME VARCHAR2(225),
    TARGET_TYPE VARCHAR2(20) NOT NULL,
    TARGET_NO   NUMBER NOT NULL
);

-- 로그인 유지(remember-me)
CREATE TABLE persistent_logins (
  username        VARCHAR(64)  NOT NULL,
  series          VARCHAR(64)  PRIMARY KEY,
  token           VARCHAR(64)  NOT NULL,
  last_used       TIMESTAMP    NOT NULL
);

